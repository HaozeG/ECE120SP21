; This program reads through each row of each character and print out the result
; converted into a desinated ASCII character. It can render a null-terminated 
; string in a large (8x16-pixel) font to the monitor. 


; R0 contains the address of the output character
; R1 contains a counter of the row
; R2 contains the address of the original character
; R3 contains the pointer in FONT_DATA
; R4 contains one row of the font data we are processing 
; R5 contains a counter of the column
; R6 is for temporary use

    .ORIG x3000

; Store the desinated ASCII character into SET_ONE and SET_ZERO for later process
    LDI   R6,SET_ONE
    ST    R6,SET_ONE
    LDI   R6,SET_ZERO
    ST    R6,SET_ZERO

; Loop through each row of the character
; Initialize the row counter 
    AND   R1,R1,#0
    ADD   R1,R1,#15
ROW_LOOP    
    LD    R2,CHAR

; Loop through each character
NEXT_CHAR
    LDR   R6,R2,#0
; If the character is x0000, that means all characters have been processed for current row.
; Otherwise, begin the preparation process for output.    
    BRz   NEXT_ROW
    LEA   R3,FONT_DATA
; Finds the beginning address of current row in FONT_DATA
    BR    FIND_CHAR

; Read through each column(each bit), convert into desinated ASCII character and print it
COLUMN
    LDR   R4,R3,#0

; Initialize the counter for the first 8 bits(8 columns) of one character in current row
    AND   R5,R5,#0
    ADD   R5,R5,#8
COLUMN_LOOP
; Evaluate the far left bit
    ADD   R4,R4,#0
; If the conditional code is set to negative, it means the far left bit is 1.
; Otherwise, it is 0. After this branch, print the selected character.
    BRn   WRITE_ONE
    LD    R0,SET_ZERO
    BR    OUTPUT 
WRITE_ONE
    LD    R0,SET_ONE
OUTPUT 
    OUT 
; R4<<1 Prepare to evaluate the next bit
    ADD   R4,R4,R4
    ADD   R5,R5,#-1
    BRp   COLUMN_LOOP

; Increment the character address for next character
    ADD   R2,R2,#1
    BR    NEXT_CHAR

; Put a linefeed to the screen to start printing the next row 
NEXT_ROW
    LD    R0,LINEFEED
    OUT 
    ADD   R1,R1,#-1
    BRzp  ROW_LOOP
    HALT

; Decompose the printing process into three parts- pointing to the correct address in FONT_DATA, evaluating the bit
; and printing desinated ASCII character.
FIND_CHAR
; The first row of current character's font data is in (R6 << 4 ) + R3
    ADD   R6,R6,R6
    ADD   R6,R6,R6
    ADD   R6,R6,R6
    ADD   R6,R6,R6
    ADD   R3,R3,R6
; Add the ROW_LOOP offset +(15-row)
    AND   R6,R6,#0
    NOT   R6,R1
; The immediate value of ADD instruction could not exceed 15. So, I split it into two instructions here.
    ADD   R6,R6,#1
    ADD   R6,R6,#15
    ADD   R3,R3,R6
    BR    COLUMN 

LINEFEED .FILL x000A
SET_ZERO .FILL x5000
SET_ONE  .FILL x5001
CHAR     .FILL x5002

